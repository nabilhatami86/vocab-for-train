export function getWordBySlug(slug: string): VocabWord | undefined {
  return vocabulary.find((w) => w.slug === slug);
}

export function getWordsByCategory(category: string): VocabWord[] {
  return vocabulary.filter((w) => w.categories.includes(category as VocabWord['categories'][number]));
}

export function getAllSlugs(): { category: string; slug: string }[] {
  return vocabulary.flatMap((word) =>
    word.categories.map((cat) => ({ category: cat, slug: word.slug }))
  );
}
