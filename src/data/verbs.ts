export type VerbItem = {
  base: string;
  v1: string;
  v2: string;
  v3: string;
  ing: string;
  meaning: string;
};

type RegularSeed = {
  base: string;
  meaning: string;
};

const VOWELS = new Set(["a", "e", "i", "o", "u"]);

function isVowel(char: string) {
  return VOWELS.has(char.toLowerCase());
}

function endsWithConsonantY(word: string) {
  if (!word.endsWith("y") || word.length < 2) return false;
  return !isVowel(word[word.length - 2]);
}

function isCvcEnding(word: string) {
  if (word.length < 3) return false;

  const chars = word.toLowerCase();
  const c1 = chars[chars.length - 3];
  const c2 = chars[chars.length - 2];
  const c3 = chars[chars.length - 1];

  if (isVowel(c1) || !isVowel(c2) || isVowel(c3)) return false;
  if (["w", "x", "y"].includes(c3)) return false;

  return true;
}

function toThirdPersonSingular(base: string) {
  if (endsWithConsonantY(base)) return `${base.slice(0, -1)}ies`;
  if (/(s|sh|ch|x|z|o)$/i.test(base)) return `${base}es`;
  return `${base}s`;
}

function toPastRegular(base: string) {
  if (endsWithConsonantY(base)) return `${base.slice(0, -1)}ied`;
  if (base.endsWith("e")) return `${base}d`;
  if (base.endsWith("c")) return `${base}ked`;
  if (isCvcEnding(base)) {
    const lastChar = base[base.length - 1];
    return `${base}${lastChar}ed`;
  }
  return `${base}ed`;
}

function toIngForm(base: string) {
  if (base.endsWith("ie")) return `${base.slice(0, -2)}ying`;
  if (base.endsWith("c")) return `${base}king`;
  if (base.endsWith("e") && !/(ee|ye|oe)$/i.test(base))
    return `${base.slice(0, -1)}ing`;
  if (isCvcEnding(base)) {
    const lastChar = base[base.length - 1];
    return `${base}${lastChar}ing`;
  }
  return `${base}ing`;
}

function buildRegularVerb(
  base: string,
  meaning: string,
  particle?: string,
  particleMeaning?: string,
): VerbItem {
  const third = toThirdPersonSingular(base);
  const past = toPastRegular(base);
  const ing = toIngForm(base);

  if (!particle) {
    return {
      base,
      v1: `${base}/${third}`,
      v2: past,
      v3: past,
      ing,
      meaning,
    };
  }

  return {
    base: `${base} ${particle}`,
    v1: `${base} ${particle}/${third} ${particle}`,
    v2: `${past} ${particle}`,
    v3: `${past} ${particle}`,
    ing: `${ing} ${particle}`,
    meaning: particleMeaning ? `${meaning} (${particleMeaning})` : meaning,
  };
}

function uniqueByBase(verbs: VerbItem[]) {
  return Array.from(new Map(verbs.map((verb) => [verb.base, verb])).values());
}

